package by.mksn.wififilehook.logic.exception;

public class CsvParseException extends Exception {

    public CsvParseException() {
        super();
    }

    public CsvParseException(String cause) {
        super(cause);
    }

    public CsvParseException(Throwable t) {
        super(t);
    }

    public CsvParseException(String cause, Throwable t) {
        super(cause, t);
    }

}
